package descent.core;

import org.eclipse.core.runtime.Plugin;
import org.osgi.framework.BundleContext;

public class DescentCompilerPlugin extends Plugin {
	
	public static String PLUGIN_ID = "descent.compiler";
	
	public static DescentCompilerPlugin instance = null;
	
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		instance = this;
	}
	
	@Override
	public void stop(BundleContext context) throws Exception {
		instance = null;
		super.stop(context);
	}
	
	public static DescentCompilerPlugin getInstance() {
		return instance;
	}
	
}
